i = 0
stop_input = False

# while False:
while (not stop_input):
    i += 1
    dict_item = {}
    print(f'\nВведите информацию о {i}-м товаре:')
    name  = input('   Название:          ')
    price = input('   Цена:              ')
    qnt   = input('   Количество:        ')
    unit  = input('   Единица измерения: ')

    dict_item['название']   = name
    dict_item['цена']       = int(price)    # надо проверить значение на число
    dict_item['количество'] = int(qnt)      # надо проверить значение на число
    dict_item['ед']         = unit
    list_goods.append( (i, dict_item))

    next_good = input('\nЗакончить ввод товаров? Y/N: ')
    if (next_good.upper() == 'Y'):
        stop_input = True
        print('Все товары введены!')
    else:
        print('Продолжаем ввод товаров!')

print('\nИтоговый список Товары: ')
for item in list_goods:
    print('   ', item)

print('')
dict_analitics = {}

for attribute in list_goods[0][1]:
    dict_analitics[attribute] = None

    set_values = set()
    for item in list_goods:
        set_values.add( item[1][attribute])

    list_values = list(set_values)
    list_values.sort()
    dict_analitics[attribute] = list_values

print('Аналитика по товарам:\n   Словарь =', dict_analitics)
print('Элементы :')
for key, value in dict_analitics.items():
    print(f'\t{key:10} -', value)