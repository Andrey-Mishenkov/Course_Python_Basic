list_goods = []
# list_goods =  [(1, {'название': 'Торт', 'цена': 650, 'количество': 17, 'ед': 'шт'}),
#                (2, {'название': 'Молоко', 'цена': 80, 'количество': 120, 'ед': 'л'}),
#                (3, {'название': 'Масло сливочное', 'цена': 140, 'количество': 220, 'ед': 'шт'}),
